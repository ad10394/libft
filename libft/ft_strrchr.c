#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (str[i])
		i++;
	while (i > 0 && str[i] != c)
		i--;
	if (str[i] == c)
		return (&str[i]);
	return (0);
}
