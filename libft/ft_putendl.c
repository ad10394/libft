#include "libft.h"

void	ft_putendl(char const *s)
{
	int		len;
	char	*p;
	char	c;

	if (s)
	{
		c = '\n';
		p = (char *)s;
		len = ft_strlen(p);
		write(1, p, len);
		write(1, &c, 1);
	}
}
